#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	result;

	if (n <= -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	result = (n % 10) + '0';
	write (fd, &result, 1);
}
